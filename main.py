def sumar_dos_numeros(numero1: int, numero2: int) -> int:
    """
    Suma de dos números y devuelve el resultado

    Args:
        numero1 (int): Primer número a sumar
        numero2 (int): Segundo número a sumar

    Returns:
        int: La suma de numero1 y numero2
    """
    return numero1 + numero2


print(sumar_dos_numeros(2, 4))
